
public class Condition 
{
	public static void main(String args[])
	{
		int number1;
		int number2;
		
		number1 = 11;
		number2 = 18;
		
		if(number1 > number2) 
		{
			System.out.printf("%d > %d", number1, number2);
		}

		if(number1 < number2) 
		{
			System.out.printf("%d < %d", number1, number2);
		}
		
		if(number1 == number2) 
		{
			System.out.printf("%d == %d", number1, number2);
		}

	}	
}
